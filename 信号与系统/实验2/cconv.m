function [f,l,r] = cconv(f1,f2,l1,r1,l2,r2,d)
%计算连续函数的卷积
%f:计算结果
%l:f范围的左端点
%r:f范围的右端点
%f1:信号1(函数句柄）
%f2:信号2（函数句柄）
%l1:函数1左端点
%r1:函数1右端点
%l2:函数2左端点
%r2:函数2右端点
%d:取样间距
l=l1+l2
r=r1+r2
x=l:d:r;
x1=l1:d:r1;
x2=l2:d:r2;
fd1=f1(x1);
fd2=f2(x2);
f=d*conv(fd1,fd2);

subplot(2,2,1);
plot(x1,fd1) 
title('f1(x)')
xlabel('x')
ylabel('f1(x)')
subplot(2,2,2);
plot(x2,fd2) 
title('f2(x)')
xlabel('x')
ylabel('f2(x)')
subplot(2,2,3);
plot(x,f); 
title('f1(x)*f2(x)')
xlabel('x')
ylabel('f(x)')
h=get(gca,'position');
h(3)=2.4*h(3);
set(gca,'position',h)
end

